public class la1q9
{
    public static void main(String[] args) 
    {
        //default always print an int
        System.out.println(5); 
        //type casted to float from int
        System.out.println((float)25/6); 
        //the ASCII value of  is added
        System.out.println(5+'6'); 
        System.out.println(5+7+'9');
        //after a string everything will be a string and vice-versa 
        System.out.println("92"+7+5); 
        System.out.println(2+"9");
    }
}